## Design of R system
# The R system is divided into 2 parts
# ~ The 'base' R system that you download from CRAN
# ~ Everthing else.

# R functionality is divedied into a number of packages.
# ~ Core part of the 'base' R system that are the most fundamental functions.
# ~ The other packages contained in the 'base' system include:
#      utils, stats, datasets, graphics, grDevices, grid, methods, tools, paralell, 
#      compiler, spines, tcltk, stats4
# ~ 'Recommend' packages: boot, class, cluster, codetools, foreign, KerSmooth, 
#      lattice, mgcv, nlme, rpart, survivial, MASS, spatial, nnet, Matrix


